# Para cada valor na sequência, atribui o valor a num e executa o bloco.
for num in ['um', 'dois', 'tres', 'quatro']:
    print(num)
       
print('FIM')

# Outro exemplo, agora com uma sequência numérica.
for num in [1, 2, 3, 4]:
    print(num)
       
print('FIM')

# Mesma coisa, mas usando a função range para gerar a sequência.
for num in range(1, 5):
    print(num)
       
print('FIM')

